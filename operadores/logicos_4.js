//Operadores JavaScript bit a bit

const num1 = 7
const num2 = 4

console.log(num1 & num2) //AND => 4 (0100)
console.log(num1 | num2) //OR => 7 (0111)
console.log(num1 ^ num2) //XOR => 3 (0011)

